package Aula08;

public class Retangulo extends Quadrilatero{
	private float altura;
	private float lado;
	public float getAltura() {
		return altura;
	}
	public void setAltura(float altura) {
		this.altura = altura;
	}
	public float getLado() {
		return lado;
	}
	public void setLado(float lado) {
		this.lado = lado;
	}
}
